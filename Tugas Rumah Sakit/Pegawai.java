class Pegawai
{
    private String nama;
    private int umur;

    public Pegawai(String nama, int umur)
    {
        this.nama=nama;
        this.umur=umur;
    }
    public String getNama()
    {
        return nama;
    }
    public int getUmur()
    {
        return umur;
    }
}